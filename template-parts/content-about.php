<section class="background">
    <h2>Background</h2>
    <p>Founded in 2003, The Wisdom Trust gained registered charity status in November 2005. Operating across the international community, we will continue to engage individuals of all age groups and backgrounds, as well as charitable organisations and private companies, to spread the word even wider and support more and more organisations, including schools, hospitals, youth groups, cultural projects, environment initiatives, sports clubs and other vital local community groups.</p>
</section>


<section class="fundraising">
<h2>Fundraising</h2>
    <p>Our fundraising total topped the £16,000 mark in 2012 with up to 70% of that income being set aside for donations to the other charitable organisations we support. A further 25% of our income is used to enhance our education programs and to help people ‘make a difference’.</p>

    <p>We operate a minimalist approach to overheads. We are run entirely by volunteers and operate from offices kindly donated to us by our friends and supporters. Our policy is to spend no more than 5% of our total income on admin and running costs.</p>
</section>

